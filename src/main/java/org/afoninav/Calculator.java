package org.afoninav;

/**
 * Калькулятор.
 */
public class Calculator {

    /**
     * Умножение двух чисел.
     * @param a множитель.
     * @param b множитель.
     * @return произведение.
     */
    public int multiply(int a, int b) {
        return a * b;
    }

    /**
     * Сложение двух чисел.
     * @param a первое слагаемое.
     * @param b второе слагаемое.
     * @return сумма.
     */
    public int summa(int a, int b) {
        return a + b;
    }
}
